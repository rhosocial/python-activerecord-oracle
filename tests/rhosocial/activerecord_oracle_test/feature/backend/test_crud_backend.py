# tests/rhosocial/activerecord_oracle_test/feature/backend/test_crud_backend.py
"""
Basic CRUD tests for Oracle backend.
"""
import uuid

from rhosocial.activerecord.backend.schema import StatementType
from rhosocial.activerecord.backend.options import ExecutionOptions


def unique_table_name(base: str = "test_crud") -> str:
    """Generate unique table name for test isolation."""
    return f"{base}_{uuid.uuid4().hex[:8]}"


class TestOracleBackendConnection:
    """Test basic Oracle backend connection functionality."""

    def test_connect(self, oracle_backend_single):
        """Test that backend connects successfully."""
        assert oracle_backend_single is not None
        assert oracle_backend_single._connection is not None

    def test_ping(self, oracle_backend_single):
        """Test ping functionality."""
        result = oracle_backend_single.ping(reconnect=False)
        assert result is True

    def test_execute_simple_query(self, oracle_backend_single):
        """Test executing a simple query."""
        result = oracle_backend_single.execute(
            "SELECT 1 AS value FROM DUAL",
            options=ExecutionOptions(stmt_type=StatementType.SELECT)
        )
        assert result is not None
        assert result.data is not None
        assert len(result.data) == 1
        # Column names are lowercased by the backend
        assert result.data[0]['value'] == 1

    def test_get_server_version(self, oracle_backend_single):
        """Test getting server version."""
        version = oracle_backend_single.get_server_version()
        assert version is not None
        assert isinstance(version, tuple)
        assert len(version) >= 2
        assert version[0] >= 18


class TestOracleBackendCRUD:
    """Test CRUD operations on Oracle backend."""

    def test_create_table(self, oracle_backend_single):
        """Test creating a table."""
        table_name = unique_table_name()

        oracle_backend_single.execute(f"""
            CREATE TABLE {table_name} (
                id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                name VARCHAR2(100) NOT NULL,
                created_at TIMESTAMP DEFAULT SYSTIMESTAMP
            )
        """, options=ExecutionOptions(stmt_type=StatementType.DDL))

        result = oracle_backend_single.execute(
            f"SELECT table_name FROM user_tables WHERE table_name = '{table_name.upper()}'",
            options=ExecutionOptions(stmt_type=StatementType.SELECT)
        )
        assert len(result.data) == 1

        # Cleanup
        oracle_backend_single.execute(
            f"DROP TABLE {table_name} PURGE",
            options=ExecutionOptions(stmt_type=StatementType.DDL)
        )

    def test_insert_and_select(self, oracle_backend_single):
        """Test inserting and selecting data."""
        table_name = unique_table_name()

        oracle_backend_single.execute(f"""
            CREATE TABLE {table_name} (
                id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                name VARCHAR2(100) NOT NULL
            )
        """, options=ExecutionOptions(stmt_type=StatementType.DDL))

        # Insert data
        oracle_backend_single.execute(
            f"INSERT INTO {table_name} (name) VALUES (:1)",
            ("test_name",),
            options=ExecutionOptions(stmt_type=StatementType.INSERT)
        )

        # Select data
        result = oracle_backend_single.execute(
            f"SELECT name FROM {table_name} WHERE name = :1",
            ("test_name",),
            options=ExecutionOptions(stmt_type=StatementType.SELECT)
        )
        assert len(result.data) == 1
        # Column names are lowercased by the backend
        assert result.data[0]['name'] == 'test_name'

        # Cleanup
        oracle_backend_single.execute(
            f"DROP TABLE {table_name} PURGE",
            options=ExecutionOptions(stmt_type=StatementType.DDL)
        )

    def test_update(self, oracle_backend_single):
        """Test updating data."""
        table_name = unique_table_name()

        oracle_backend_single.execute(f"""
            CREATE TABLE {table_name} (
                id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                name VARCHAR2(100) NOT NULL
            )
        """, options=ExecutionOptions(stmt_type=StatementType.DDL))
        oracle_backend_single.execute(
            f"INSERT INTO {table_name} (name) VALUES (:1)",
            ("original",),
            options=ExecutionOptions(stmt_type=StatementType.INSERT)
        )

        # Update
        oracle_backend_single.execute(
            f"UPDATE {table_name} SET name = :1 WHERE name = :2",
            ("updated", "original"),
            options=ExecutionOptions(stmt_type=StatementType.UPDATE)
        )

        # Verify
        result = oracle_backend_single.execute(
            f"SELECT name FROM {table_name}",
            options=ExecutionOptions(stmt_type=StatementType.SELECT)
        )
        assert len(result.data) == 1
        # Column names are lowercased by the backend
        assert result.data[0]['name'] == 'updated'

        # Cleanup
        oracle_backend_single.execute(
            f"DROP TABLE {table_name} PURGE",
            options=ExecutionOptions(stmt_type=StatementType.DDL)
        )

    def test_delete(self, oracle_backend_single):
        """Test deleting data."""
        table_name = unique_table_name()

        oracle_backend_single.execute(f"""
            CREATE TABLE {table_name} (
                id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                name VARCHAR2(100) NOT NULL
            )
        """, options=ExecutionOptions(stmt_type=StatementType.DDL))
        oracle_backend_single.execute(
            f"INSERT INTO {table_name} (name) VALUES (:1)",
            ("to_delete",),
            options=ExecutionOptions(stmt_type=StatementType.INSERT)
        )

        # Delete
        oracle_backend_single.execute(
            f"DELETE FROM {table_name} WHERE name = :1",
            ("to_delete",),
            options=ExecutionOptions(stmt_type=StatementType.DELETE)
        )

        # Verify
        result = oracle_backend_single.execute(
            f"SELECT COUNT(*) AS cnt FROM {table_name}",
            options=ExecutionOptions(stmt_type=StatementType.SELECT)
        )
        # Column names are lowercased by the backend
        assert result.data[0]['cnt'] == 0

        # Cleanup
        oracle_backend_single.execute(
            f"DROP TABLE {table_name} PURGE",
            options=ExecutionOptions(stmt_type=StatementType.DDL)
        )
